object VarTypes {
    
    def main(args: Array[String]) = {
        val a: Double = 7
        var b: Float = 7
        var c: Long = 7
        var d: Integer = 7
        var e: Short = 7
        var f: Byte = 7
        var g: Char = '7'
        var h: Boolean = true
        var i: String = "Es un 7"
        //var j: Unit = 7
        println(s"This is a Double: $a")
        println(s"This is a Float: $b")
        println(s"This is a Long: $c")
        println(s"This is a Integer: $d")
        println(s"This is a Short: $e")
        println(s"This is a Byte: $f")
        println(s"This is a Char: $g")
        println(s"This is a Boolean: $h")
        println(s"This is a String: $i")   
    }
}